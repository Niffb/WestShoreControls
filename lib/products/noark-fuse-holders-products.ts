// Helper functions
export const getFuseHolderProductsBySeries = (series: string): Product[] => {
  return noarkFuseHoldersData.filter(product => 
    product.model.includes(series.toUpperCase())
  );
};

export const getFuseHolderProductsByPoles = (poles: string): Product[] => {
  return noarkFuseHoldersData.filter(product => 
    product.model.includes(`${poles}P`)
  );
};

export const getFuseHolderProductsByVoltage = (voltage: string): Product[] => {
  return noarkFuseHoldersData.filter(product => 
    product.model.includes(voltage)
  );
};

export const getAllFuseHolderSeries = (): string[] => {
  return Array.from(new Set(noarkFuseHoldersData.map(product => product.model.split('-')[0])));
};

export const getFuseHolderSeriesInfo = (seriesName: string) => {
  const seriesProducts = getFuseHolderProductsBySeries(seriesName);
  return {
    name: seriesName,
    count: seriesProducts.length,
    products: seriesProducts
  };
};


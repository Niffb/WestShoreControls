// Helper functions
export const getEnclosedBreakerProductsBySeries = (series: string): Product[] => {
  return noarkEnclosedBreakersData.filter(product => 
    product.specs?.some(spec => spec.toLowerCase().includes(`series: ${series.toLowerCase()}`))
  );
};

export const getEnclosedBreakerProductsByEnclosureType = (enclosureType: string): Product[] => {
  return noarkEnclosedBreakersData.filter(product => 
    product.specs?.some(spec => spec.toLowerCase().includes(`nema enclosure: ${enclosureType.toLowerCase()}`))
  );
};

export const getEnclosedBreakerProductsByCurrentRange = (minCurrent: number, maxCurrent: number): Product[] => {
  return noarkEnclosedBreakersData.filter(product => {
    const currentSpec = product.specs?.find(spec => spec.includes('Current Rating:'));
    if (currentSpec) {
      const currentMatch = currentSpec.match(/(\d+)A/);
      if (currentMatch) {
        const current = parseInt(currentMatch[1]);
        return current >= minCurrent && current <= maxCurrent;
      }
    }
    return false;
  });
};

export const getEnclosedBreakerProductsByPoles = (poles: string): Product[] => {
  return noarkEnclosedBreakersData.filter(product => 
    product.specs?.some(spec => spec.toLowerCase().includes(`pole configuration: ${poles.toLowerCase()}`))
  );
};

export const getAllEnclosedBreakerSeries = (): string[] => {
  return enclosedBreakerSeries.map(series => series.name);
};

export const getEnclosedBreakerSeriesInfo = (seriesName: string) => {
  return enclosedBreakerSeries.find(series => 
    series.name.toLowerCase().includes(seriesName.toLowerCase())
  );
};


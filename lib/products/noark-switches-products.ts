// Helper functions
export const getSwitchesProductsBySeries = (series: string): Product[] => {
  return noarkSwitchesProducts.filter(product => 
    product.specs?.some(spec => spec.toLowerCase().includes(`subcategory: ${series.toLowerCase()}`))
  );
};

export const getSwitchesProductsByCurrentRange = (minCurrent: number, maxCurrent: number): Product[] => {
  return noarkSwitchesProducts.filter(product => {
    const currentSpec = product.specs?.find(spec => spec.includes('Current Rating:'));
    if (currentSpec) {
      const currentMatch = currentSpec.match(/(\d+)A/);
      if (currentMatch) {
        const current = parseInt(currentMatch[1]);
        return current >= minCurrent && current <= maxCurrent;
      }
    }
    return false;
  });
};

export const getAllSwitchesSeries = (): string[] => {
  return switchesSeries.map(series => series.name);
};

export const getSwitchesSeriesInfo = (seriesName: string) => {
  return switchesSeries.find(series => 
    series.name.toLowerCase().includes(seriesName.toLowerCase())
  );
};

